  def __init__(self):
    pass

  def _log_trace(self, log_entry):
    print(log_entry)

  def _log_debug(self, log_entry):
    print(log_entry)

  def _log_info(self, log_entry):
    print(log_entry)

  def _log_warn(self, log_entry):
    print(log_entry)

  def _log_error(self, log_entry):
    print(log_entry)

  def _log_fatal(self, log_entry):
    print(log_entry)